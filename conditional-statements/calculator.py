num_1 = int(input("enter the first number!"))
num_2 = int(input("enter the second number!"))

operation = input("enter your operator + , -, *, /")
if operation == "+":
    print(num_1 + num_2)
elif operation == "-":
    print(num_1 - num_2)
elif operation == "*":
    print(num_1 * num_2)
elif operation == "/":
    print(num_1 / num_2)
